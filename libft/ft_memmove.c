#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	size_t	i;

	i = 0;
	if (dest == src || n == 0)
		return (dest);
	if (dest < src)
	{
		while (i < n)
		{
			((char *)dest)[i] = ((char *)src)[i];
			i++;
		}
	}
	else
	{
		i = 1;
		while (i <= n)
		{
			((char *)dest)[n - i] = ((char *)src)[n - i];
			i++;
		}
	}
	return (dest);
}
